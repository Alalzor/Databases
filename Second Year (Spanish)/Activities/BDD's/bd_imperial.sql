SET NAMES 'utf8mb4';
DROP DATABASE IF EXISTS db_imperial;
CREATE DATABASE db_imperial CHARACTER SET utf8mb4 COLLATE utf8mb4_es_0900_as_cs;
USE db_imperial;

CREATE TABLE oficiales (
    id_oficial INT PRIMARY KEY AUTO_INCREMENT,
    nombre VARCHAR(100) NOT NULL UNIQUE,
    rango VARCHAR(50),
    cargo VARCHAR(100)
);

CREATE TABLE celdas (
    id_celda INT PRIMARY KEY,
    sector INT,
    nivel INT,
    capacidad INT
);

CREATE TABLE criminales (
    id_criminal INT PRIMARY KEY AUTO_INCREMENT,
    nombre VARCHAR(100) NOT NULL,
    especie VARCHAR(50),
    causa_detencion TEXT,
    libertad BOOLEAN DEFAULT FALSE,
    estado BOOLEAN DEFAULT TRUE,
    CONSTRAINT pri_nosp_uk UNIQUE (nombre, especie)
);

CREATE TABLE ubicacion (
    id_criminal INT,
    id_celda INT,
    fecha_entrada DATE,
    fecha_salida DATE,
    PRIMARY KEY (id_criminal, id_celda),
    FOREIGN KEY (id_criminal) REFERENCES criminales(id_criminal),
    FOREIGN KEY (id_celda) REFERENCES celdas(id_celda)
);

CREATE TABLE misiones (
    id_mision INT PRIMARY KEY AUTO_INCREMENT,
    nombre_mision VARCHAR(100) UNIQUE,
    descripcion TEXT,
    objetivo VARCHAR(500),
    id_oficial INT,
    FOREIGN KEY (id_oficial) REFERENCES oficiales(id_oficial)
);

CREATE TABLE troopers (
    id_trooper CHAR(6) PRIMARY KEY,
    serie INT,
    rango VARCHAR(20)
);

CREATE TABLE asignacion (
    id_mision INT,
    id_trooper CHAR(6),
    PRIMARY KEY (id_mision, id_trooper),
    FOREIGN KEY (id_mision) REFERENCES misiones(id_mision),
    FOREIGN KEY (id_trooper) REFERENCES troopers(id_trooper)
);

